/*Las funciones getter están destinadas simplemente a devolver (obtener) el valor de la variable privada de un objeto al usuario 
sin que el usuario acceda directamente a la variable privada.

Las funciones de establecimiento están destinadas a modificar (establecer) el valor de la variable privada de un objeto en función 
del valor pasado a la función de establecimiento. Este cambio podría implicar cálculos, o incluso sobrescribir el valor anterior por completo.

Observe la sintaxis utilizada para invocar getter y setter. Ni siquiera parecen funciones. Los getters y setters son importantes 
porque ocultan detalles de implementación internos.
Nota: Es una convención preceder el nombre de una variable privada con un guión bajo (_). Sin embargo, la práctica en sí no hace que 
una variable sea privada.*/ 

